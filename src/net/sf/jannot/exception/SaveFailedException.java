package net.sf.jannot.exception;

public class SaveFailedException extends Exception {

    public SaveFailedException() {
        super();
       
    }

    public SaveFailedException(String arg0, Throwable arg1) {
        super(arg0, arg1);
       
    }

    public SaveFailedException(String arg0) {
        super(arg0);
   
    }

    public SaveFailedException(Throwable arg0) {
        super(arg0);
      
    }

    /**
     * 
     */
    private static final long serialVersionUID = -2022595560772769970L;

}
